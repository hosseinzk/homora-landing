const FLAGS = { en:'🇬🇧', fi:'🇫🇮', ru:'🇷🇺', uk:'🇺🇦' }
const NAMES = { en:'EN', fi:'FI', ru:'RU', uk:'UA' }
let currentLang = localStorage.getItem('homora_lang') || 'en'

function setLang(lang) {
  currentLang = lang
  localStorage.setItem('homora_lang', lang)
  document.documentElement.lang = lang

  const flagEl = document.getElementById('currentFlag')
  const nameEl = document.getElementById('currentLangName')
  if (flagEl) flagEl.textContent = FLAGS[lang]
  if (nameEl) nameEl.textContent = NAMES[lang]

  ;['en','fi','ru','uk'].forEach(l => {
    const el = document.getElementById('check-' + l)
    if (el) el.style.display = l === lang ? 'inline' : 'none'
    const opt = el?.parentElement
    if (opt) opt.classList.toggle('active', l === lang)
  })

  const dict = T[lang] || T.en
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n')
    if (dict[key] !== undefined) el.textContent = dict[key]
  })
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html')
    if (dict[key] !== undefined) el.innerHTML = dict[key]
  })

  const switcher = document.getElementById('langSwitcher')
  if (switcher) switcher.classList.remove('open')

  document.body.style.fontFamily = (lang === 'ru' || lang === 'uk')
    ? "'Inter', 'Arial', sans-serif"
    : "'Inter', sans-serif"
}

function toggleLang() {
  const s = document.getElementById('langSwitcher')
  if (s) s.classList.toggle('open')
}

document.addEventListener('click', e => {
  const switcher = document.getElementById('langSwitcher')
  if (switcher && !switcher.contains(e.target)) switcher.classList.remove('open')
})
